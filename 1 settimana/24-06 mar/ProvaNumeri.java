public class ProvaNumeri {

    //stampo dei numeri e delle operazioni tra numeri
    public static void main (String[] args){
        //stampo dei numeri interi e il risultato di operazioni tra coppie di interi
        System.out.println(3);
        System.out.println(358);
        System.out.println(5000);
        System.out.println(3+3);
        System.out.println(3*2);

         //dichiaro due variabili e ne stampo la somma
        int x = 5;
        int y = 6;
        System.out.println(x+y);
        
        //stampo la somma di tre variabili
        int a = 5, b = 6, c = 50;
        System.out.println(a+b+c);

        // a tre variabili lo stesso valore in una riga
        int m,n,o;
        m = n = o = 50;
        System.out.println(m+n+o);
    
    }
}
